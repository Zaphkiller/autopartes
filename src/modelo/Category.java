
package modelo;

public class Category {
    
    private int id_category;
    private String code_category;
    private String name_category;
    private String status_category;     

    public int getId_category() {
        return id_category;
    }

    public void setId_category(int id_category) {
        this.id_category = id_category;
    }

    public String getCode_category() {
        return code_category;
    }

    public void setCode_category(String code_category) {
        this.code_category = code_category;
    }

    public String getName_category() {
        return name_category;
    }

    public void setName_category(String name_category) {
        this.name_category = name_category;
    }

    public String getStatus_category() {
        return status_category;
    }

    public void setStatus_category(String status_category) {
        this.status_category = status_category;
    }
    
    
    
}
        