
package modelo;


public class Cliente {
    
    private int id_cliente;
    private String name_cliente;
    private String lastname_cliente;
    private String dni_cliente;
    private String phone_cliente;
    private String address_cliente;
    private String status_cliente;

    public int getId_cliente() {
        return id_cliente;
    }

    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }

    public String getName_cliente() {
        return name_cliente;
    }

    public void setName_cliente(String name_cliente) {
        this.name_cliente = name_cliente;
    }

    public String getLastname_cliente() {
        return lastname_cliente;
    }

    public void setLastname_cliente(String lastname_cliente) {
        this.lastname_cliente = lastname_cliente;
    }

    public String getDni_cliente() {
        return dni_cliente;
    }

    public void setDni_cliente(String dni_cliente) {
        this.dni_cliente = dni_cliente;
    }

    public String getPhone_cliente() {
        return phone_cliente;
    }

    public void setPhone_cliente(String phone_cliente) {
        this.phone_cliente = phone_cliente;
    }

    public String getAddress_cliente() {
        return address_cliente;
    }

    public void setAddress_cliente(String address_cliente) {
        this.address_cliente = address_cliente;
    }

    public String getStatus_cliente() {
        return status_cliente;
    }

    public void setStatus_cliente(String status_cliente) {
        this.status_cliente = status_cliente;
    }
    
    
    
}
